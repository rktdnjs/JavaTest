
public class Main {
    public static void main(String args[]) {
        /* 조건문과 반복문 98 if문
        프로그램의 흐름(flow)를 바꾸는 역할을 하는 문장들을 '제어문(control statement)'이라고 한다.
        if문은 가장 기본적인 조건문이다.
        */
        
        int score = 80;
        
        if (score > 77) {
            System.out.println("그에게 주어지는 합격목걸이...");
        }
	}
}