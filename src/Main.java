import java.util.*;

public class Main {
    public static void main(String args[]) {
        /* while 문
        for문에 비해 while문은 구조가 간단하다.
        if문처럼 조건식과 블럭{} 만으로 이루어져 있다.
        다만 while문은 조건식이 참인동안 계속 반복하는 점을 유의하자.
        */
        
        //while문 예제
        
        int i = 5;
        
        while(i--!=0) {
            System.out.println(i + " - I can do it.");
        }
        }
    
	}